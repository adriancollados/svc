#include "calculadora.h"


double sumar(double n1, double n2){
	double total;

	total = n1 + n2;

	return total;
}

double restar(double n1, double n2){
	double total;

	total = n1 - n2;

	return total;
}

double multiplicar(double n1, double n2){
	double total;

	total = n1 * n2;

	return total;
}

double dividir(double n1, double n2) {
	double total;

	total = n1 / n2;

	return total;
}

double raiz(double n1){
	double total;

	total = sqrt(n1);

	return total;
}

